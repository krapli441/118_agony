// * 1. 변수 선언 시 선언되지 않은 변수를 사용하면 ReferenceError가 발생한다.
// * 2. delete 연산자로 전역 객체의 변수를 삭제할 수 없다.
// * 3. 함수 내에서 this를 undefined로 설정합니다. (non-strict mode에서는 전역 객체를 참조한다.)
// * 4. 함수 내에서 중복된 매개변수 이름을 사용할 수 없다.
// * 5. with 문을 사용할 수 없다.
// * 6. eval 함수로 생성된 코드는 엄격 모드가 적용된다.

// * 'use strict'는 스크립트 파일 전체 또는 함수의 시작 부분에 작성하여
// * 해당 범위에서 엄격 모드를 활성화할 수 있다. 전체 스크립트에 적용하려면
// * 스크립트 파일의 가장 위에 작성하면 되며, 함수 내에서만 적용하려면 해당 함수의 첫 줄에 작성하면 된다.


//* 자바스크립트에서 변수를 어떻게 선언하는지 예를 들어 설명해보세요. - 프로그래밍 공통

let a
var b
const c = "상수"

// 자바스크립트에서는 변수를 선언할 때 let, var, const 키워드를 사용한다.
// let은 변수를 선언할 때 사용하는 키워드이다. 변수의 값을 변경할 수 있다.
// var는 변수를 선언할 때 사용하는 키워드이다. 이 역시 변수의 값을 변경할 수 있다.
// const는 상수를 선언할 때 사용하는 키워드이다. 상수는 한 번 선언하면 값을 변경할 수 없다.

// 실생활을 예로 들자면, 접시와 밀폐용기를 생각하면 된다.
// let과 var는 빈 접시이기 때문에 안의 내용물을 언제든지 바꿀 수 있지만
// 상수 const는 내용물을 한번 담으면 밀폐하기 때문에 내용물을 바꿀 수 없다.